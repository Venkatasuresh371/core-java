package com.jsp.operators;

public class ArithmeticOperators
{
	public static void main(String[] args)
	{
		int a=10;
		int b=20;
		System.out.println("c = "+(a+b));
		System.out.println("c = "+(a-b));
		System.out.println("c = "+(a*b));
		System.out.println("c = "+(a/b));
		System.out.println("c = "+(a%b));	
	}
}
